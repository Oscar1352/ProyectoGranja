package Granja;

public class CaracteristicasDeLaGranja {
    private int Oro;
    private int Alimento;
    private int DuraciónDePartida;
    private int CantidadDeCeldasCompradas;

    public int getOro() {
        return Oro;
    }

    public void setOro(int oro) {
        Oro = oro;
    }

    public int getAlimento() {
        return Alimento;
    }

    public void setAlimento(int alimento) {
        Alimento = alimento;
    }

    public int getDuraciónDePartida() {
        return DuraciónDePartida;
    }

    public void setDuraciónDePartida(int duraciónDePartida) {
        DuraciónDePartida = duraciónDePartida;
    }

    public int getCantidadDeCeldasCompradas() {
        return CantidadDeCeldasCompradas;
    }

    public void setCantidadDeCeldasCompradas(int cantidadDeCeldasCompradas) {
        CantidadDeCeldasCompradas = cantidadDeCeldasCompradas;
    }
}
