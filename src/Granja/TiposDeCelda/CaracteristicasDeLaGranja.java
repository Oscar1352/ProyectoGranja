package Granja.TiposDeCelda;

public class CaracteristicasDeLaGranja {
    private static int Oro;
    private static int Alimento;
    private static int DuraciónDePartida;
    private static int CantidadDeCeldasCompradas;

    public static int getOro() {
        return Oro;
    }

    public static void setOro(int oro) {
        Oro = oro;
    }

    public static int getAlimento() {
        return Alimento;
    }

    public static void setAlimento(int alimento) {
        Alimento = alimento;
    }

    public static int getDuraciónDePartida() {
        return DuraciónDePartida;
    }

    public static void setDuraciónDePartida(int duraciónDePartida) {
        DuraciónDePartida = duraciónDePartida;
    }

    public static int getCantidadDeCeldasCompradas() {
        return CantidadDeCeldasCompradas;
    }

    public static void setCantidadDeCeldasCompradas(int cantidadDeCeldasCompradas) {
        CantidadDeCeldasCompradas = cantidadDeCeldasCompradas;
    }
}
